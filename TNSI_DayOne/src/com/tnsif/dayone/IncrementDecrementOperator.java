package com.tnsif.dayone;

public class IncrementDecrementOperator {
public static void main(String[] args) {
	
	
	int a=10;
	int b=20;
	++a;
	int c=++a + b+a--; 
	System.out.println(c);
	System.out.println(a);
	
	/*
	 
	 Incremental operator 
	 	Increase current value by 1
	 	2 types
	 		Pre-Increment
	 			Increases current value before operation
	 		Post-Increment
	 			Perform operation before increment
	 
	Decremental operator 
	 	Decreases current value by 1
	 	2 types
	 		Pre-Increment
	 			Decreases current value before operation
	 		Post-Increment
	 			Perform operation before decrement
	 		
	 */
}
}
