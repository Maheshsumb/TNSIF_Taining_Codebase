package com.tnsif.day3.loops;

public class WhileLoop {

	public static void main(String[] args) {
		
		/*
		  While loop:
		  	- Used when we don't know how many time we have to execute the code 
		  	- It is an Entry control loop
		  	- Executed until condition becomes false
		  	
		 */
		
		int i = 1;

		while (i <= 10) {

			System.out.println(i * 2);
			i++;
		}

	}

}
