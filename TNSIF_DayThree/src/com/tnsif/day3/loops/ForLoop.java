package com.tnsif.day3.loops;

public class ForLoop {

	public static void main(String[] args) {
		
		/* For loop:
		 	- Used when we have to perform known number of iteration 
		 	- It is Entry control loop
		 	- Executed until the condition becomes false  
		 */
			
		for (int i = 1; i <= 10; i++) {
			System.out.println(2 * i);
		}
	}

}
